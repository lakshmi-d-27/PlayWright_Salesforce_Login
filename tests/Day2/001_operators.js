/**
 * Operators:
 * Arithmetic Operators
 * 
 * 1. Addition (+)
 * 2. Subtraction (-)
 * 3. Multiplication (*)
 * 4. Division (/)
 * 5. Modulus (%)
 * 
 * 
 * 
 */
// Addition:

let a = 10;
let b = 20;
let sum = a + b;
console.log(sum); // 30

// Subtraction: 
let c = 20;
let d = 10;
let sub = c - d;
console.log(sub); // 10

// Multiplication:
let e = 10; 
let f = 20;
let mul = e * f;
console.log(mul); // 200

// Division:
let g = 20;
let h = 10;
let div = g / h;
console.log(div); // 2

// Modulus:
let i = 20;
let j = 10;
let mod = i % j;

console.log(mod); // 0

/**
 * Increment operators
 * 
 * 1. Pre Increment (++a) - Increment symbol before the variable
 * 2. Post Increment (a++) - Increment symbol after the variable
 *    
 * 
 */

let k = 10;
let preIncrement = ++k;
console.log(preIncrement); // 11

let l = 10;     
let postIncrement = l++;
console.log(postIncrement); // 10